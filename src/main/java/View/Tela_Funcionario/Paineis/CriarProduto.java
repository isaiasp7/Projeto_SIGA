    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {
            String nome = txtNome.getText().trim();
            int quantidade = Integer.parseInt(txtQuantidade.getText().trim());
            double preco = Double.parseDouble(txtPreco.getText().trim());

            Empresa fornecedorSelecionado = (Empresa) boxFornecedor.getSelectedItem();
            if (fornecedorSelecionado == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "Selecione um fornecedor v√°lido.");
                return;
            }
            
            long idFornecedor = fornecedorSelecionado.getId();

            Produto novo = new Produto(nome, quantidade, idFornecedor, preco);

            ProdutoDAO dao = new ProdutoDAO();
            boolean sucesso = dao.createProduto(novo);

            if (sucesso) {
                javax.swing.JOptionPane.showMessageDialog(this, "Produto criado com sucesso!");
                dispose(); // Fecha o di√°logo
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Erro ao criar produto.");
            }

        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Insira valores v√°lidos nos campos!");
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Erro inesperado: " + e.getMessage());
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

